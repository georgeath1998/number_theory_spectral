rho = 0.5 + i * gamma;

Lambda_k = zeros(size(k_vals));

for idx = 1:length(k_vals)
    k = k_vals(idx);
    sum_rho = 0;
    for j = 1:length(rho)
        sum_rho = sum_rho + k^rho(j);
    end
    Lambda_k(idx) = -(2*pi / T) * real(sum_rho);
end
figure;
plot(k_vals, Lambda_k, 'b-');
xlabel('k');
ylabel('Lambda(k) (approx)');
title('Spectral approximation of Lambda(k)');
grid on;
